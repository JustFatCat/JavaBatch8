package com.syntax.class01;

public class PrintlnVsPrint {

	public static void main(String[] args) {
		// println --> prints and creates a new line
		// print --> print and no new line is created
		
		System.out.println("I'm 21 years old");
		
		System.out.println("I'm very happy");
		
		System.out.print("I'm learning new skills");
		
		System.out.print("I'm cat");

	}

}
